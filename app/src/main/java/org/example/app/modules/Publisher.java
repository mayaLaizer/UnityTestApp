package org.example.app.modules;


//Publisher object for all the publisher data
public class Publisher {

    String name;
    String email;

    public Publisher(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
